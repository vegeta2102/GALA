@OptIn(
    ExperimentalMaterial3Api::class,
    ExperimentalComposeUiApi::class,
    ExperimentalMaterial3AdaptiveApi::class,
)
@Composable
fun GalaApp(
    modifier: Modifier = Modifier,
    appState: GalaAppState,
) {
    val snackbarHostState = remember { SnackbarHostState() }
    val isOffline = appState.isOffline.collectAsStateWithLifecycle()
    val currentDestination = appState.currentDestination
    val message = stringResource(id = R.string.no_connection)
    LaunchedEffect(key1 = isOffline) {
        snackbarHostState.showSnackbar(
            message = message,
            duration = SnackbarDuration.Indefinite
        )
    }
    GalaNavigationSuiteScaffold(
        navigationSuiteItems = {
            appState.topLevelDestinations.forEach { destination ->
                val selected = currentDestination
                    .isTopLevelDestinationInHierarchy(destination)
                item(
                    selected = selected,
                    onClick = {
                        // TODO
                    },
                    icon = {
                        Icon(
                            imageVector = destination.unselectedIcon,
                            contentDescription = null,
                        )
                    },
                    selectedIcon = {
                        Icon(
                            imageVector = destination.selectedIcon,
                            contentDescription = null,
                        )
                    },
                    label = { Text(stringResource(destination.iconTextId)) },
                )
            }
        }
    ) {

        Scaffold { padding ->
            Text(text = "Hello", Modifier.padding(padding))
        }

    }
}

private fun NavDestination?.isTopLevelDestinationInHierarchy(destination: TopLevelDestination) =
    this?.hierarchy?.any {
        it.route?.contains(destination.name, true) ?: false
    } ?: false